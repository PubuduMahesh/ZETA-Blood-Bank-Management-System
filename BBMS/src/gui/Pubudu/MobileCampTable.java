    private void addTable() throws SQLException {

        controller con = new controller();
        Connection conn = con.createConnection();

        String pre_month = calculatePreviousMonthDate();

        String query = "SELECT * FROM bloodcamp having date >= '" + pre_month + "'";
        ResultSet rs = con.executeQuery(conn, query);

        String cpId = null;
        String place = null;
        String date = null;

        while (rs.next()) {
            //String ///

            cpId = rs.getString("CAMP_ID");
            place = rs.getString("PLACE");
            date = rs.getString("DATE");
            
            System.out.println(date);
                    
            
            Object[] obj = {cpId, place, date};
            dtm.addRow(obj);

        }

    }

    public String calculatePreviousMonthDate() {

        DateFormat dateFormat = new SimpleDateFormat("YYYY/MM/dd");
        Date date = new Date();
        Calendar calendar = Calendar.getInstance();
        calendar.setTime(date);
        calendar.add(Calendar.DAY_OF_MONTH, -30);
        Date Previousmonthdate = calendar.getTime();
        String pre_month = dateFormat.format(Previousmonthdate);

        return pre_month;

    }
